    private void guardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_guardarMouseClicked
        
        guardar.setEnabled(false);
        
        try {
            // obtener datos del text field
            String medidaProducto=medida.getText().trim(); // Eliminar espacios en blanco al inicio y al final
            
            //validacion de datos
            if (medidaProducto.isEmpty()) {
                
                JOptionPane.showMessageDialog(null,"Por favor, ingrese una nueva Medida");
                medida.requestFocus();// Enfocar el campo de texto para que el usuario pueda corregirlo
                return;// Salir del m√©todo sin guardar
                
            }
            
            MedidaProducto existenteMedida=medidaProductoDao.obtenerPorMedida(medidaProducto);
            
            if (existenteMedida==null) {
                MedidaProducto medidaProductoEntidad=new MedidaProducto();
                medidaProductoEntidad.setMedida(medidaProducto);
                medidaProductoDao.guardar(medidaProductoEntidad);
                
                JOptionPane.showMessageDialog(null,"Datos almacenados correctamente");
                
                medida.setText("");
                
            } else {
                JOptionPane.showMessageDialog(null,"Medida ya registrada, por favor ingrese otra medida");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Datos no almacenados");
        } finally {
            guardar.setEnabled(true);// habilita el boton guardar
        }
    }//GEN-LAST:event_guardarMouseClicked


